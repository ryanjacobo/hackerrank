package Primitives;

public class VariablesLesson {
    public static void main(String[] args){
        // Variables are primitive types of data
        int myNumber; // initialize, int are whole numbers
        short myShort = 624;
        long myLong = 9797;
        double myDouble = 7.3243;
        char myChar = '*'; // 1 character - number, letter, symbol
        boolean myBoolean = true;
        byte myByte = 127;

        myNumber = 24; // declare

        System.out.println(myNumber);
        System.out.println(myDouble);
        System.out.println(myChar);
        System.out.println(myByte);
    }
}
