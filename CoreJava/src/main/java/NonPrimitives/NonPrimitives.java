package NonPrimitives;

public class NonPrimitives {

    // type 'main' to autofill
    public static void main(String[] args) {
        // Non-primitives are always capitalized
        String text;
        text = "Non-primitive variable";
        String text2 = " ";
        String text3 = "combine strings";

        String combinedText = text + text2 + text3;

        System.out.println(combinedText);
        System.out.println("Hello" + " Kipster" + "!");

        // concatenate
        System.out.println("Concatenate: " + text);
    }
}
